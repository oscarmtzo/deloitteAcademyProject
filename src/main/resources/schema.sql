ALTER TABLE PETS ADD FOREIGN KEY (SPECIE_ID) REFERENCES SPECIES(SPECIE_ID);
ALTER TABLE PET_VACCINE ADD FOREIGN KEY (PET_ID) REFERENCES PETS(PET_ID);
